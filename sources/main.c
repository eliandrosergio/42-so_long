#include "so_long.h"

static int	argv_checker(char *argv)
{
	int	i;

	if (!argv)
		return (0);
	i = 0;
	while (argv[i])
		i++;
	i -= 1;
	if (argv[i] == 'r' && argv[i - 1] == 'e' && argv[i - 2] == 'b'
		&& argv [i - 3] == '.')
		return (1);
	return (0);
}

void	ft_erro_msg(char *str)
{
	ft_printf_colored("\033[0;31m", "Erro!\n");
	ft_printf(str);
}

int	main(int argc, char **argv)
{
	t_game	game;

	if (argc == 2)
	{
		game.map = read_map(argv[1]);
		if (map_checker(&game) && argv_checker(argv[1]))
		{
			game_init(&game);
			gameplay(&game);
			mlx_loop(game.mlx);
		}
		else
		{
			if (game.map)
				free_map(game.map);
			ft_erro_msg("Mapa Inválido.\n");
			exit(1);
		}
	}
	else
	{
		ft_erro_msg("Argumentos Inválidos.\n");
		exit(1);
	}
	return (0);
}
